package com;

public class FirstJavaClass {
    // we must never use keywords anywhere randomly
    // int if = 10; invalid

    // int break = 30; compilation error

    // if you use keywords while creating methods ten it is invalid
    // public void super (){ }

    // case sensitivity
    int salim = 10;
    int SALIM = 10;

    //alphanumeric values accepted
    int Salim23456 = 10;

    // identifiers can never start with digits
    // int 1234salim = 10; invalid

    // $ and _ special characters are only accepted
    int Salim$23456 = 10;
    int Salim_23456 = 10;

    // @ special character cannot be accepted
    // int salim@23456 = 10; invalid

    int salim_1234 = 20;
    int TEST = 30;
    int test = 40;
    int test$123 = 50;

   // int test@123 = 60; invalid
   // int test#1234 = 70; invalid

    // spaces in between identifiers name are not allowed
    // int digital partner = 80; invalid

    // int 1234digital = 90; invalid
     int digital1234 = 100;

     // long identifiers name are not recommended
     int digitaldtdtudyfyijgiygiulgiulgiugiulgiylfyilfyilfiffiyfiyfiy = 67;

    // int return = 10; invalid

    public static void main(String[] args) {
        System.out.println("hello java");
        System.out.println("Hello this is my first code in java!!!");
    }


}
