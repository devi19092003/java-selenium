package com;

public class BackslashLiterals {
    public static void main(String[] args) {

        // Backslash Literals :-

        // to start with a new line :- (\n)

        System.out.println("Hello everyone!\nMy hobby is decorating gardens or outdoors." +
                "\nWelcome to my new rose garden!!!!");

        // to give horizontal tabs :- (\t)

        System.out.println("10\t20\t30\t40\t50\t60\t70\t80\t90\t100");
        System.out.println("11\t22\t33\t44\t55\t66\t77\t88\t99\t110");

        // for blank space :- (\b)

        System.out.println("FESTIVALDUSSHERA");
        System.out.println("FESTIVAL   \b\bDUSSHERA");

        // to enclose any word within single quotes :- (\')

        System.out.println("Hi everyone! welcome to \'Java\' ");

        // to enclose any word within double quotes :- (\")

        System.out.println("Good morning everyone\n Today's thought is\n" +
                "\"Honesty is the best policy\"");

        System.out.println("Flower\'s");

        // Primitive data types :-

        //1- boolean data type :-

        boolean bool1 = true;
        boolean bool2 = false;
        System.out.println(bool1);
        System.out.println(bool2);

        //2- byte data type :-

        byte byte1 = 00000001;
        byte byte2 = 2;
        byte byte3 = 3;
        System.out.println(byte1);
        System.out.println(byte2);
        System.out.println(byte3);

        //3- char data type :-

        char character1 = 'A';
        char character2 = 'c';
        char character3 = '@';
        System.out.println(character1);
        System.out.println(character2);
        System.out.println(character3);

        //4- short data type :-

        short short1 = 2435;
        System.out.println(short1);

        //5- int data type :-

        int number1 = 244354550;
        System.out.println(number1);

        //6- long data type :-

        long long1 = 1222536365;
        System.out.println(long1);

        //7- float data type :-

        float float1 = 12.234554656757676878789798788f;
        System.out.println(float1);

        //8- double data type :-

        double double1 = 24.4354657657687897989898989808088d;
        System.out.println(double1);

        /* hi i am a multi line comment
           hello this is a good practice
        * */
    }
}
